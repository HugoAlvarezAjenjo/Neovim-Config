-- Left Colunm
vim.opt.number = true -- Display line numbers
vim.opt.relativenumber = true -- Display relative line numbers
vim.opt.numberwidth = 2 -- Line number width
vim.opt.signcolumn = "yes" -- Always display sign column
vim.opt.wrap = true -- Line wrapping
vim.opt.scrolloff = 5 -- Lines of context

-- Tabs Spacing
vim.opt.shiftwidth = 4 -- Number of spaces inserted for each indentation
vim.opt.tabstop = 4 -- Number of spaces for tabs character
vim.opt.softtabstop = 4 -- Number of spaces for tabs key
vim.opt.expandtab = true -- Use spaces instead of tabs
vim.opt.smartindent = true -- Smart indentation
vim.opt.breakindent = true -- Wrap lines

-- General
vim.opt.fileencoding = "utf-8" -- File encoding
vim.opt.mouse = "a" -- Enable mouse
vim.opt.showmode = true -- Show mode
